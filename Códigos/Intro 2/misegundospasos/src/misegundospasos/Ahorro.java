package misegundospasos;

public class Ahorro {

	public static void main(String[] args) {
		
		int ahorro=0;
		
		ahorro+=100; // 100
		ahorro+=150; // 250
		ahorro+=50;  // 300
		ahorro+=200; // 500
		ahorro*=2;   // 1000
		
		System.out.print("\n\t Ahorro: $"+ahorro);
		
	}
	

}
